#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *s))
{
	int i;

	i = 0;
	if (s && f)
	{
		while (*s)
		{
			f(i, s);
			i++;
			s++;
		}
	}
}
