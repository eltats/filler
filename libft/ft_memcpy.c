#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char	*a;
	unsigned char	*b;

	a = (unsigned char *)dest;
	b = (unsigned char *)src;
	if (!(a) && !(b))
		return (NULL);
	while (n-- > 0)
		*a++ = *b++;
	return (dest);
}
